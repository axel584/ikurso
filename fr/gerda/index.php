				<p class="rimarko"><strong>REMARQUES</strong> : </p>
				<ol>
					<li>Dans les exemples ci-dessus, là où la semi-voyelle est suivie par une
					voyelle (deux dernières colonnes), on considère généralement qu’elle
					appartient à la syllabe suivante où elle reprend son rôle de consonne.
					Il n’y a donc pas diphtongue au sens phonétique du terme.</li>
					
					<li>La réalisation exacte des diphtongues peut varier selon les
					locuteurs. Certains auront tendance à conserver une prononciation
					consonantique de la semi-voyelle, surtout s’il s’agit de <b>j</b>, alors que
					d’autres la prononcent comme une voyelle très brève plus ou moins fondue
					avec le premier élément. Par exemple pour la diphtongue <b>aj</b>, on peut
					aussi bien se baser sur la prononciation de l’anglais <em>my</em>, ou de
					l’allemand <em>mein</em>, que sur celle du français <em>maille</em>, de l’espagnol
					<em>hay</em> ou du russe <em>raï</em>.</li>
	
					<li>Les autres combinaisons de voyelle et de semi-voyelle sont
					extrêmement rares. La combinaison <b>oŭ</b> se trouve dans quelques mots. La
					combinaison <b>ij</b> se trouve dans des noms propres russes translittérés
					(par exemple <span class="eo">Ĉajkovskij</span>).</li>
	
					<li>Les combinaisons <span class="eo">ai</span>, <span class="eo">au</span>, <span class="eo">oi</span>, 
