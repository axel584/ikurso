				<p class="rimarko"><strong>REMARQUES</strong> :</p> 
				<ol>
					<li>Le <b>e</b> et le <b>o</b> sont des voyelles «moyennes», plus ou moins ouvertes
					selon les locuteurs et le contexte. Les <b>è</b> et <b>o</b> ouverts du français
					en sont une bonne approximation.</li>
					<li>Les voyelles ont une durée «moyenne», sensiblement équivalente à
					celle des voyelles brèves du français. Cependant il est très courant
					d’allonger sensiblement une voyelle lorsqu’elle termine une syllabe
					accentuée&nbsp;: le <b>e</b> de <span class="eo"><u>ple</u>na</span> est souvent prononcé plus long que celui de
					<span class="eo"><u>ver</u>da</span>.</li>
					<li>Il n’y a pas de voyelle nasale en espéranto. La première syllabe de
					<span class="eo"><u>gran</u>da</span> rime en français avec <em>Anne</em>, 
					pas avec <b>an</b>; <span class="eo"><u>ka</u>fon</span> rime avec
					<em>téléphone</em>, pas avec <em>carafon</em>, etc.</li>
					<li>Toutes les voyelles forment syllabe, même <b>i</b> et <b>u</b> accolés à une
					autre voyelle. Il faut donc prononcer <span class="eo">kio</span> : <span class="eo"><u>ki</u>-o</span> (deux syllabes),
					<span class="eo">foiro</span> : <span class="eo">fo-<u>i</u>-ro</span> (trois syllabes), 
					<span class="eo">lui</span> : <span class="eo"><u>lu</u>-i</span>.</li>
				</ol>

