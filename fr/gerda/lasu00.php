				<h3 id="avant-propos">Avant-propos</h3>
			
				<p>On ne connaît pas vraiment l’espéranto si l’on n’est pas capable de tirer parti des potentialités présentes dans les bases minimales du vocabulaire et de la grammaire. L’élève apprend un nombre très restreint de nouvelles racines dans chaque chapitre de <i>Gerda malaperis!</i> mais leur potentiel expressif est immense. C’est donc l’un des buts de ce recueil que de l’aider à découvrir cette richesse.</p>
				<p>Pour atteindre ce but il est conseillé à l’élève qui vient d’étudier un chapitre de <i>Gerda malaperis!</i> de lire immédiatement le texte de même numéro de ce recueil. Il connaîtra tout ce qui est nécessaire, au niveau grammatical et lexical, pour comprendre ce texte. Par exemple, s’il a appris la matière des chapitres&nbsp;1 à 7 inclus, il ne rencontrera dans le texte numéro&nbsp;7 rien qu’il n’ait déjà étudié antérieurement. Ou plutôt il n’y rencontrera aucune nouveauté grammaticale ni aucune racine nouvelle mais il sera confronté à des mots nouveaux. La nécessité d’en deviner le sens, sur la base des éléments déjà acquis, l’initiera progressivement au système de dérivation et de 
				composition de l’espéranto.</p>
				<p>Affermir les connaissances acquises, tel est le second but de ce recueil. La totalité du matériau linguistique de <i>Gerda malaperis!</i> y est réutilisée et les racines qui n’apparaissent pas assez souvent dans le roman y sont répétées suffisamment pour qu’elles se fixent dans la mémoire de l’élève avec un minimum d’effort de sa part. L’effet de fixation se trouvera ici renforcé par le fait que les éléments en question sont employés dans des textes dont l’atmosphère et les thèmes sont tout autres que dans le roman.</p>
				<p>Ce recueil se compose pour l’essentiel de monologues. L’élève peut ainsi s’exercer seul&nbsp;: en lisant le texte à haute voix il utilisera une langue parlée authentique et il pourra s’identifier au personnage tout comme le ferait un acteur. Ce type d’identification est un des ressorts psychologiques connus pour favoriser l’assimilation d’une langue.</p>
				<p>Entre les numéros&nbsp;13 et 24, le recueil présente deux textes en parallèle à chaque chapitre de <i>Gerda malaperis!</i>. L’élève aura loisir de choisir le morceau qui lui plaît le mieux au premier coup d’œil, à moins qu’il ne veuille lire les deux.</p>
				<p>Si, grâce à ces exemples d’espéranto parlé, rédigés avec un vocabulaire limité, l’élève arrive à prendre conscience des possibilités multiples et diverses que lui offrent ces éléments minimaux de la langue pour s’exprimer, alors le but principal de cet ouvrage aura été atteint.</p>
		
				<p>Claude Piron (traduction MB)</p>
			
